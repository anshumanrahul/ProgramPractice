package javaBasicPrograms;

public class IfelseifBlockProgram {

	public static void main(String[] args) {
		String city="Pune";
		
		if(city=="Delhi")
		{
			System.out.println("City name is: "+city);
		}
		else if(city=="Pune")
		{
			System.out.println("City name is: "+city);
		}
		else
		{
			System.out.println("City is: "+city);
		}

	}

}
